<script>
    // Автоматичне підсвічування активного пункту меню
    (function () {
        // Чекаємо поки DOM завантажиться
        if (document.readyState === 'loading') {
            document.addEventListener('DOMContentLoaded', highlightActiveNav);
        } else {
            highlightActiveNav();
        }

        function highlightActiveNav() {
            const currentPath = window.location.pathname;

            document.querySelectorAll('.nav-link').forEach(link => {
                const href = link.getAttribute('href');

                // Видаляємо старий active клас
                link.classList.remove('active');

                // Перевіряємо чи це активне посилання
                if (href === currentPath) {
                    link.classList.add('active');
                }
                // Для головної сторінки /admin
                else if (href === '/admin' && currentPath === '/admin') {
                    link.classList.add('active');
                }
                // Для шляхів на кшталт /admin/users (починаються з)
                else if (href && href !== '/admin' && currentPath.startsWith(href)) {
                    link.classList.add('active');
                }
            });
        }
    })();
</script>